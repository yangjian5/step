package com.aiwsport.core.entity;

public class Activedata {
    private Integer id;

    private Integer activesstepid;

    private String issign;

    private Integer sumstep;

    private Integer daystep;

    private String type;

    private String status;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getActivesstepid() {
        return activesstepid;
    }

    public void setActivesstepid(Integer activesstepid) {
        this.activesstepid = activesstepid;
    }

    public String getIssign() {
        return issign;
    }

    public void setIssign(String issign) {
        this.issign = issign;
    }

    public Integer getSumstep() {
        return sumstep;
    }

    public void setSumstep(Integer sumstep) {
        this.sumstep = sumstep;
    }

    public Integer getDaystep() {
        return daystep;
    }

    public void setDaystep(Integer daystep) {
        this.daystep = daystep;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}